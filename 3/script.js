libraries = ["Requests", "Django", "Flask", "BeautifulSoup", "Selenium",
             "NumPy", "Pandas", "Matplotlib", "OpenCV", "PtQt5", "Pygame"]

const keys = Object.keys(videos)

const container = document.getElementById('container')
var content = ''
var navContent = '<div class="module"><h3 class="module-title">Sections</h3><ul class="module-content">'

for (var i = 0; i < keys.length; i++) {
  navContent += `<li><a href="#${i}">${i} - ${keys[i]}</a></li>`

  content += `
  <div id="${i}" class="module">
    <h3 class="module-title">${keys[i]}</h3>
      <ul class="module-content">`

  const lessons = Object.values(videos[keys[i]])
  for (var j = 0; j < lessons.length; j++) {
    content += `<li><a onclick="changeVideo(${i}, ${j})">Lesson ${j+1}: ${lessons[j].title}</a></li>`
  }
  content += '</ul></div>'
}

content += '<h3>Useful Libraries in Python</h3>'
content += `
  <div class="module">
    <h3 class="module-title">Resources</h3>
      <ul class="module-content">`

for (var i = 0; i < libraries.length; i++) {
  content += `<li><a>${libraries[i]}</a></li>`
}
content += '</ul></div>'

container.innerHTML += navContent + '</ul></div>' + content

const videoTitle = document.getElementById('video-title')
const videoFrame = document.getElementById('video') 

function changeVideo(sect, lesson) {
  const videoInfo = Object.values(videos[keys[sect]])[lesson]
  videoTitle.textContent = videoInfo.title
  videoFrame.src = `https://drive.google.com/file/d/${videoInfo.url}/preview`
  
  window.scrollTo({
    top: 0,
    behavior: 'smooth'
  });
}

changeVideo(0, 0)